    public void llenarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Línea");
        modelo.addColumn("Nombre");
        modelo.addColumn("Estado");
        modelo.addColumn("Comisión");
        
        LineaDAO dao = new LineaDAO();
        List<clsLinea> lista = dao.listar();
        
        Object[] fila = new Object[4];
        for (clsLinea linea : lista) {
            fila[0] = linea.getLineaId();
            fila[1] = linea.getLinNombre();
            // Convertimos el boolean a "1" o "0" para mostrarlo en la tabla
            fila[2] = linea.isLinEstado() ? "1" : "0"; 
            fila[3] = linea.getLinComision();
            modelo.addRow(fila);
        }
        tablaLineas.setModel(modelo);
    }

