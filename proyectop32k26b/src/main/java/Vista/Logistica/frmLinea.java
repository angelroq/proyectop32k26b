    private void btnModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificar1ActionPerformed
        try {
            if (txtId.getText().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Selecciona una línea primero.");
                return;
            }

            clsLinea linea = new clsLinea();
            LineaDAO dao = new LineaDAO();

            linea.setLineaId(Integer.parseInt(txtId.getText()));
            linea.setLinNombre(txtNombre.getText());
            
            boolean estado = txtEstado.getText().trim().equals("1");
            linea.setLinEstado(estado);
            
            linea.setLinComision(new java.math.BigDecimal(txtComision.getText()));

            if (dao.actualizar(linea)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Línea actualizada correctamente.");
                limpiarCampos();
                llenarTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al actualizar en la BD.");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Verifique los datos ingresados.");
        }
    }//GEN-LAST:event_btnModificar1ActionPerformed

    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        String idEliminar = txtId.getText();

        if(idEliminar.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar una línea para eliminar.");
            return;
        }

        int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, "¿Eliminar línea ID " + idEliminar + "?", "Advertencia", javax.swing.JOptionPane.YES_NO_OPTION);
        if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
            LineaDAO dao = new LineaDAO();
            if (dao.eliminar(Integer.parseInt(idEliminar))) {
                javax.swing.JOptionPane.showMessageDialog(this, "Línea eliminada.");
                limpiarCampos();
                llenarTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar. Verifique que ningún producto use esta línea.");
            }
        }

    }//GEN-LAST:event_btnEliminar1ActionPerformed

