    private void btnRegistrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrar1ActionPerformed
try {
            clsLinea linea = new clsLinea();
            LineaDAO dao = new LineaDAO();

            linea.setLinNombre(txtNombre.getText());
            
            // Si el texto es "1", lo guarda como true. Si es otra cosa, como false.
            boolean estado = txtEstado.getText().trim().equals("1");
            linea.setLinEstado(estado); 
            
            linea.setLinComision(new java.math.BigDecimal(txtComision.getText()));

            if (dao.insertar(linea)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Línea registrada con éxito.");
                limpiarCampos();
                llenarTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error al registrar la línea.");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Verifica que la comisión sea un número válido.");
        }

    }//GEN-LAST:event_btnRegistrar1ActionPerformed

