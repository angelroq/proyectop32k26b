    private void btnBuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar1ActionPerformed

        String idBuscado = txtbuscado1.getText().trim(); 
        
        if(idBuscado.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Ingrese el ID a buscar.");
            return;
        }

        LineaDAO dao = new LineaDAO();
        clsLinea linea = dao.buscarPorId(Integer.parseInt(idBuscado));

        if (linea != null) {
            txtId.setText(String.valueOf(linea.getLineaId()));
            txtNombre.setText(linea.getLinNombre());
            txtEstado.setText(linea.isLinEstado() ? "1" : "0");
            txtComision.setText(linea.getLinComision().toString());
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Línea no encontrada.");
        }
    }//GEN-LAST:event_btnBuscar1ActionPerformed

