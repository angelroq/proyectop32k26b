    public void llenarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Marca");
        modelo.addColumn("Nombre");
        modelo.addColumn("Estado");
        modelo.addColumn("Comisión"); // Columna nueva
        
        MarcaDAO dao = new MarcaDAO();
        List<clsMarca> lista = dao.listar();
        
        Object[] fila = new Object[4]; // Ahora son 4 datos
        for (clsMarca marca : lista) {
            fila[0] = marca.getMarcaid();
            fila[1] = marca.getMarnombre();
            fila[2] = marca.getMarestado();
            fila[3] = marca.getMarcomision(); // Dato nuevo
            modelo.addRow(fila);
        }
        tablaMarcas.setModel(modelo); 
    }

