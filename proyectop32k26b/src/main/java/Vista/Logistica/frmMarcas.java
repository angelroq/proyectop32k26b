    private void btnBuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar1ActionPerformed
       // Asumiendo que tienes un txtBuscado
        String idBuscado = txtbuscado1.getText().trim();
        
        if(idBuscado.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese el ID de la Marca para buscar.");
            return;
        }

        MarcaDAO dao = new MarcaDAO();
        clsMarca marca = dao.buscarPorId(Integer.parseInt(idBuscado));

        if (marca != null) {
            txtId.setText(String.valueOf(marca.getMarcaid()));
            txtNombre.setText(marca.getMarnombre());
            txtEstado.setText(String.valueOf(marca.getMarestado()));
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró la marca con ID: " + idBuscado);
        }
    }//GEN-LAST:event_btnBuscar1ActionPerformed

