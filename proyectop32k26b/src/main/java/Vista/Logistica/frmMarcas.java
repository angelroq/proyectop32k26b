    private void btnModificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificar1ActionPerformed
        try {
            if (txtId.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Selecciona una marca de la tabla para modificar.");
                return;
            }

            clsMarca marca = new clsMarca();
            MarcaDAO dao = new MarcaDAO();

            marca.setMarcaid(Integer.parseInt(txtId.getText()));
            marca.setMarnombre(txtNombre.getText());
            marca.setMarestado(Integer.parseInt(txtEstado.getText()));
            marca.setMarcomision(new java.math.BigDecimal(txtComision.getText())); // Se envía la comisión

            if (dao.actualizar(marca)) {
                JOptionPane.showMessageDialog(null, "Marca actualizada correctamente.");
                limpiarCampos();
                llenarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar en la base de datos.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Verifique los datos numéricos.");
        }
    }//GEN-LAST:event_btnModificar1ActionPerformed

    private void btnEliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminar1ActionPerformed
        String idEliminar = txtId.getText();

        if(idEliminar.isEmpty()){
            JOptionPane.showMessageDialog(null, "Debe seleccionar una marca de la tabla.");
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(null, "¿Eliminar marca con ID " + idEliminar + "?", "Advertencia", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            MarcaDAO dao = new MarcaDAO();
            if (dao.eliminar(Integer.parseInt(idEliminar))) {
                JOptionPane.showMessageDialog(null, "Marca eliminada.");
                limpiarCampos();
                llenarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar. (Puede que haya productos usando esta marca).");
            }
        }

    }//GEN-LAST:event_btnEliminar1ActionPerformed

