    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (IdVendedor.getText().isEmpty() || FechaInicial.getText().isEmpty() || FechaFinal.getText().isEmpty()) {
        // Muestra un mensaje de advertencia al usuario si falta algún dato
        JOptionPane.showMessageDialog(this, "Debe completar el ID y el rango de fechas (AAAA-MM-DD)");
        return; // Detiene la ejecución del método
    }

    try {
        //  Captura los datos ingresados en la interfaz y los guarda en variables
        int id = Integer.parseInt(IdVendedor.getText()); // Convierte el texto del ID a número entero
        String f1 = FechaInicial.getText(); // Guarda la fecha inicial como cadena de texto
        String f2 = FechaFinal.getText(); // Guarda la fecha final como cadena de texto

        //  Obtiene el modelo de la tabla actual y elimina todas las filas existentes para refrescarla
        DefaultTableModel modelo = (DefaultTableModel) tblReporte.getModel();
        modelo.setRowCount(0); 

        // Instancia la clase DAO para acceder a los métodos de la base de datos
        ReportesDAO dao = new ReportesDAO();
        
        //  Llama al método de filtrado y guarda los resultados en una lista de objetos
        List<clsReportes> lista = dao.listarPorFiltro(id, f1, f2);

        // Variable para acumular la suma de todas las comisiones encontradas
        double sumaComisiones = 0;

        // Recorre la lista de resultados para ir agregando cada registro a la tabla
        for (clsReportes r : lista) {
            // Crea un arreglo de objetos con los datos que se mostrarán en las columnas
            Object[] fila = {
                r.getRepid(),        // 1. Id Reporte
                r.getVenid(),        // 2. # Vendedor
                r.getVen_nombre(),   // 3. Vendedor
                r.getRepfecha(),     // 4. Fecha de Registro
                r.getRephora(),      // 5. Hora
                r.getComcomision()   // 6. Comisión
            };
            modelo.addRow(fila); // Agrega la fila al modelo de la tabla
            sumaComisiones += r.getComcomision(); // Suma el valor al total acumulado
        }

        //  Si la lista no está vacía, agrega una fila final con el total general del periodo
        if (!lista.isEmpty()) {
            // Se dejan vacíos los primeros campos y se pone el total en la columna correspondiente
            modelo.addRow(new Object[]{"", "", "TOTAL COMISION:", sumaComisiones});
        } else {
            // Informa al usuario si la consulta no devolvió ningún registro
            JOptionPane.showMessageDialog(this, "No hay registros para este vendedor en las fechas indicadas.");
        }

    } catch (NumberFormatException e) {
        // Captura el error en caso de que el usuario escriba letras en el campo de ID
        JOptionPane.showMessageDialog(this, "El ID del vendedor debe ser un número válido.");
    } catch (Exception e) {
        // Captura cualquier otro error inesperado durante el proceso
        JOptionPane.showMessageDialog(this, "Error al procesar la consulta: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton4ActionPerformed


