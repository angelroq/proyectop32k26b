    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {

            clsPlanillaEncabezado p = new clsPlanillaEncabezado();

            // ================= FECHA =================
            if (txtFecha.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese la fecha (yyyy-MM-dd)");
                return;
            }

            Date utilDate = new SimpleDateFormat("yyyy-MM-dd")
                    .parse(txtFecha.getText());

            p.setPlafecha(new java.sql.Date(utilDate.getTime()));

            // ================= TOTAL =================
            if (txtTotal.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese el total");
                return;
            }

            p.setPlatotal(Double.parseDouble(txtTotal.getText()));

            // ================= ESTADO =================
            int estado = cmbEstado.getSelectedItem().toString().equals("ACTIVO") ? 1 : 0;
            p.setPlaestado(estado);

            // ================= MOVIMIENTO =================
            Object item = cmbMovimientoBancario.getSelectedItem();

            if (item == null || item.toString().equals("SIN MOVIMIENTO")) {
                JOptionPane.showMessageDialog(this, "Seleccione un movimiento bancario válido");
                return;
            }

            int movId = Integer.parseInt(item.toString().split(" - ")[0]);
            p.setMovbid(movId);

            // ================= GUARDAR =================
            boolean ok = dao.insertar(p);

            if (ok) {
                JOptionPane.showMessageDialog(this, "Guardado correctamente");
                listarPlanillas();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al guardar");
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

