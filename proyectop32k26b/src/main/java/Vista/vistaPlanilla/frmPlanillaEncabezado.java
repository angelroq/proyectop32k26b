    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        try {

            int codigo = Integer.parseInt(txtBuscar.getText());

            clsPlanillaEncabezado p = dao.buscar(codigo);

            if (p != null) {

                txtCodigo.setText(String.valueOf(p.getPlacodigo()));

                txtFecha.setText(
                        new SimpleDateFormat("yyyy-MM-dd").format(p.getPlafecha())
                );

                txtTotal.setText(String.valueOf(p.getPlatotal()));

                // Estado
                cmbEstado.setSelectedItem(p.getPlaestado() == 1 ? "ACTIVO" : "INACTIVO");

                // Movimiento bancario (FIX)
                String buscar = p.getMovbid() + " -";

                for (int i = 0; i < cmbMovimientoBancario.getItemCount(); i++) {
                    if (cmbMovimientoBancario.getItemAt(i).toString().startsWith(buscar)) {
                        cmbMovimientoBancario.setSelectedIndex(i);
                        break;
                    }
                }

            } else {
                JOptionPane.showMessageDialog(this, "Registro no encontrado");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        try {

            clsPlanillaEncabezado p = new clsPlanillaEncabezado();

            p.setPlacodigo(Integer.parseInt(txtCodigo.getText()));

            Date utilDate = new SimpleDateFormat("yyyy-MM-dd")
                    .parse(txtFecha.getText());

            p.setPlafecha(new java.sql.Date(utilDate.getTime()));

            p.setPlatotal(Double.parseDouble(txtTotal.getText()));

            int estado = cmbEstado.getSelectedItem().toString().equals("ACTIVO") ? 1 : 0;
            p.setPlaestado(estado);

           //--------------------------------------
            Object item = cmbMovimientoBancario.getSelectedItem();

            int movId = 0;

            if (item != null && !item.toString().equals("SIN MOVIMIENTO")) {
                try {
                    movId = Integer.parseInt(item.toString().split(" - ")[0]);
                } catch (Exception ex) {
                    movId = 0;
                }
            }

            p.setMovbid(movId);
            

            boolean ok = dao.modificar(p);

            if (ok) {
                JOptionPane.showMessageDialog(this, "Modificado correctamente");
                listarPlanillas();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al modificar");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

