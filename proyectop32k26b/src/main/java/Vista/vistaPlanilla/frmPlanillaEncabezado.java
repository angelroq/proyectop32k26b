    private void cargarMovimientoBancario() {

        cmbMovimientoBancario.removeAllItems();

        MovimientoBancarioDAO daoMov = new MovimientoBancarioDAO();

        List<clsMovimientoBancario> lista = daoMov.seleccionar();
        System.out.println("Tamaño lista movimientos: " + lista.size());

        if (lista.isEmpty()) {
            cmbMovimientoBancario.addItem("SIN MOVIMIENTO");
            return;
        }

        for (clsMovimientoBancario m : lista) {
            cmbMovimientoBancario.addItem(
                    m.getMovbid() + " - " + m.getMovdescripcion()
            );
        }
    }

    

