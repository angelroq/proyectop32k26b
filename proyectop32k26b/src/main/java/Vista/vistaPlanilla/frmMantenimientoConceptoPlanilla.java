    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        try {

            clsConceptosPlanillas cp = new clsConceptosPlanillas();

            cp.setCodigo(
                    Integer.parseInt(txtCodigo.getText())
            );

            cp.setNombre(txtNombre.getText());

            cp.setTipo(
                    cmbTipo.getSelectedItem().toString()
            );

            cp.setPorcentaje(
                    Double.parseDouble(txtPorcentaje.getText())
            );

            cp.setMonto(
                    Double.parseDouble(txtMonto.getText())
            );

            cp.setAplica(
                    cmbAplica.getSelectedItem().toString()
            );

            int estado = cmbEstado.getSelectedItem().toString()
                    .equals("ACTIVO") ? 1 : 0;

            cp.setEstado(estado);

            boolean respuesta = dao.modificar(cp);

            if (respuesta) {

                JOptionPane.showMessageDialog(
                        null,
                        "Registro modificado correctamente"
                );

                listarConceptos();
                limpiarCampos();

            } else {

                JOptionPane.showMessageDialog(
                        null,
                        "Error al modificar"
                );
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    null,
                    "Error: " + e.getMessage()
            );
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {

            int codigo = Integer.parseInt(txtCodigo.getText());

            int respuesta = JOptionPane.showConfirmDialog(
                    null,
                    "¿Desea eliminar el registro?"
            );

            if (respuesta == 0) {

                boolean eliminado = dao.eliminar(codigo);

                if (eliminado) {

                    JOptionPane.showMessageDialog(
                            null,
                            "Registro eliminado"
                    );

                    listarConceptos();
                    limpiarCampos();

                } else {

                    JOptionPane.showMessageDialog(
                            null,
                            "Error al eliminar"
                    );
                }
            }

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    null,
                    "Error: " + e.getMessage()
            );
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

