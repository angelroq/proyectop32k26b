    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
 try {

        if (CodigoPuesto.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Seleccione un puesto"
            );

            return;
        }

        if (NombrePuesto.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Ingrese el nombre"
            );

            return;
        }

        if (salario.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(
                    null,
                    "Ingrese salario"
            );

            return;
        }

        clsPuesto puesto = new clsPuesto();

        puesto.setPuecodigo(
                Integer.parseInt(
                        CodigoPuesto.getText()
                )
        );

        puesto.setPuenombre(
                NombrePuesto.getText().trim()
        );

        puesto.setPuesalarioBase(
                new java.math.BigDecimal(
                        salario.getText().trim()
                )
        );

        PuestoDAO dao = new PuestoDAO();

        clsBitacora bitacora = new clsBitacora();

        int resultado = dao.actualizarPuesto(
                puesto,
                bitacora
        );

        if (resultado > 0) {

            JOptionPane.showMessageDialog(
                    null,
                    "Registro modificado"
            );

            tblPuesto.setModel(
                    dao.listarPuestosEnTabla()
            );

            CodigoPuesto.setText("");
            NombrePuesto.setText("");
            salario.setText("");
            txtBuscar.setText("");

        } else {

            JOptionPane.showMessageDialog(
                    null,
                    "Error al modificar"
            );
        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                "Error: " + e.getMessage()
        );

        e.printStackTrace();
    }
    }//GEN-LAST:event_btnModificarActionPerformed

