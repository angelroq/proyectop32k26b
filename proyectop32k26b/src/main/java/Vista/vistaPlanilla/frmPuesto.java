    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
      // 1. Validar que el campo no esté vacío para evitar errores de número
    if (CodigoPuesto.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Debe seleccionar o buscar un registro para eliminar.");
        return;
    }

    // 2. Confirmación opcional (es buena práctica)
    int respuesta = JOptionPane.showConfirmDialog(null, "¿Está seguro de eliminar este puesto?", "Confirmar", JOptionPane.YES_NO_OPTION);
    
    if (respuesta == JOptionPane.YES_OPTION) {
        try {
            clsPuesto puesto = new clsPuesto();
            // Convertimos el texto a entero
            puesto.setPuecodigo(Integer.parseInt(CodigoPuesto.getText().trim()));

            PuestoDAO dao = new PuestoDAO();
            clsBitacora bitacora = new clsBitacora();

            // Ejecutar la eliminación
            int resultado = dao.eliminarPuesto(puesto, bitacora);

            if (resultado > 0) {
                JOptionPane.showMessageDialog(null, "Registro eliminado correctamente");

                // 3. Limpiar campos y refrescar la tabla visual
                limpiarCampos(); // Método que limpia los JTextField
                llenarTabla();   // <--- ESTA LÍNEA ES CLAVE PARA QUE SE REFLEJE EL CAMBIO

                // 4. Resetear estados de botones
                NombrePuesto.setEnabled(false);
                salario.setEnabled(false);
                btnGuardar.setEnabled(false);
                btnModificar.setEnabled(false);
                btnEliminar.setEnabled(false);
                btnNuevo.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo eliminar el registro");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El código no es válido.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

