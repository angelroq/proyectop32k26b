     private clsBitacora crearBitacora(String accion) {

        clsBitacora bitacora = new clsBitacora();

        try {

            BitacoraDAO dao = new BitacoraDAO();

            int usuario = 1;

            try {
                usuario = clsUsuarioConectado.getUsuId();
            } catch (Exception e) {
                System.out.println("Usuario no definido");
            }

            bitacora.setUsucodigo(usuario);

            bitacora.setAplcodigo(2);

            bitacora.setBitfecha(dao.fechaActual());

            bitacora.setBitip(java.net.InetAddress.getLocalHost().getHostAddress());

            bitacora.setBitequipo(java.net.InetAddress.getLocalHost().getHostName());

            bitacora.setBitaccion(accion);

        } catch (Exception e) {
            e.printStackTrace();
        }

        return bitacora;
    }

    
    public void llenadoDeTablas() {

        DefaultTableModel modelo = new DefaultTableModel();

        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        modelo.addColumn("DPI");
        modelo.addColumn("Puesto");
        modelo.addColumn("Fecha Ingreso");
        modelo.addColumn("Estado");

        tblEmpleados.setModel(modelo);

        EmpleadosDAO dao = new EmpleadosDAO();

        clsBitacora bitacora = crearBitacora("Consulta empleados");

        List<clsEmpleados> lista = dao.obtenerEmpleados(bitacora);

        String[] dato = new String[6];

        for (clsEmpleados e : lista) {

            dato[0] = String.valueOf(e.getEmpcodigo());
            dato[1] = e.getEmpnombre();
            dato[2] = e.getEmpdpi();
            dato[3] = String.valueOf(e.getPuecodigo());
            dato[4] = String.valueOf(e.getEmpfecha_ingreso());
            dato[5] = String.valueOf(e.getEmpestado());

            modelo.addRow(dato);
        }
    }
    
    
