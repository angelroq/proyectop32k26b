public void llenadoDeCombos() {
        cmbAcreedor.removeAllItems();
        listaAcreedores.clear();
        cmbAcreedor.addItem("Seleccione un acreedor");
        try {
            clsAcreedor filtro = new clsAcreedor();
            filtro.setAcreEstado("A");
            listaAcreedores = new clsAcreedor().getBuscarAcreedoresPorEstado(filtro);
            for (clsAcreedor a : listaAcreedores) {
                cmbAcreedor.addItem(a.getAcreCodigo() + " - " + a.getAcreNombre());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                "Error al cargar acreedores:\n" + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
 
    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("N° Factura");
        modelo.addColumn("Acreedor");
        modelo.addColumn("Fecha");
        modelo.addColumn("Subtotal");
        modelo.addColumn("IVA");
        modelo.addColumn("Total");
        modelo.addColumn("Estado");
 
        clsFacturaCompraAcreedor fac = new clsFacturaCompraAcreedor();
        List<clsFacturaCompraAcreedor> lista = fac.getListadoFacturasCompra();
        tablaFacturas.setModel(modelo);
 
        for (clsFacturaCompraAcreedor f : lista) {
            modelo.addRow(new Object[]{
                f.getFacComId(),
                f.getFacComNumero(),
                buscarNombreAcreedor(f.getAcreCodigo()),
                f.getFacComFecha(),
                "Q " + f.getFacComSubtotal(),
                "Q " + f.getFacComIva(),
                "Q " + f.getFacComTotal(),
                f.getFacComEstado()
            });
        }
    }
 
    private String buscarNombreAcreedor(int acreCodigo) {
        for (clsAcreedor a : listaAcreedores) {
            if (a.getAcreCodigo() == acreCodigo) return a.getAcreNombre();
        }
        return String.valueOf(acreCodigo);
    }
 
