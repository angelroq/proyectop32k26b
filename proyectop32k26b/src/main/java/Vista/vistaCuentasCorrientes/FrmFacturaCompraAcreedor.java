    private void btsRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsRegistrarActionPerformed
        // TODO add your handling code here:
    if (cmbAcreedor.getSelectedIndex() <= 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un acreedor.",
                "Validación", JOptionPane.WARNING_MESSAGE); return;
        }
        if (txtNumFactura.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el número de factura.",
                "Validación", JOptionPane.WARNING_MESSAGE); return;
        }
        double total;
        try {
            total = Double.parseDouble(txtTotal.getText().trim());

    if (total <= 0) {
        throw new NumberFormatException();
    }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese un total válido mayor a 0.",
                "Validación", JOptionPane.WARNING_MESSAGE);
            txtTotal.requestFocus(); return;
        }
 
       double subtotal = total / 1.12;
       double iva = total - subtotal;

        int acreCod = listaAcreedores.get(cmbAcreedor.getSelectedIndex() - 1).getAcreCodigo();
 
        clsFacturaCompraAcreedor fac = new clsFacturaCompraAcreedor();
        fac.setFacComNumero(txtNumFactura.getText().trim());
        fac.setFacComFecha(txtFecha.getText());
        fac.setProCodigo(999);
        fac.setAcreCodigo(acreCod);
        fac.setFacComSubtotal(subtotal);
        fac.setFacComIva(iva);
        fac.setFacComTotal(total);
        fac.setFacComEstado("Vigente");
 
        int confirm = JOptionPane.showConfirmDialog(null,
            "¿Confirmar registro de factura?\n\n" +
      
                    "Acreedor  : " + cmbAcreedor.getSelectedItem() + "\n" +
            "N° Factura: " + fac.getFacComNumero()          + "\n" +
            "Subtotal  : Q " + subtotal                     + "\n" +
            "IVA       : Q " + iva                          + "\n" +
            "Total     : Q " + total,
            "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;
 
        int nuevoId = fac.setIngresarFacturaCompra(fac);
        if (nuevoId > 0) {
            facComIdActual = nuevoId;
            txtIdFactura.setText(String.valueOf(nuevoId));
            txtEstado.setText("Vigente");
            JOptionPane.showMessageDialog(null,
                "Registro Ingresado\n",
                "Información del Sistema", JOptionPane.INFORMATION_MESSAGE);
            new BitacoraDAO().insert(idUsuario, 4025, "Insertar Factura Acreedor");
            llenadoDeTablas();
            limpiarTextos();
        } else {
            JOptionPane.showMessageDialog(null,
                "Error al registrar. Verifique que el N° de factura no esté duplicado.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_btsRegistrarActionPerformed

    private void btsBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsBuscarActionPerformed
        // TODO add your handling code here:
    if (txtbuscado.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el ID a buscar.",
                "Validación", JOptionPane.WARNING_MESSAGE); return;
        }
        try {
            clsFacturaCompraAcreedor filtro = new clsFacturaCompraAcreedor();
            filtro.setFacComId(Integer.parseInt(txtbuscado.getText().trim()));
            clsFacturaCompraAcreedor enc = filtro.getBuscarFacturaPorId(filtro);
 
            if (enc == null) {
                JOptionPane.showMessageDialog(null,
                    "No se encontró factura con ID: " + txtbuscado.getText(),
                    "Sin resultados", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            cargarFacturaEnForm(enc);
            new BitacoraDAO().insert(idUsuario, 4026, "Buscar Factura Acreedor");
 
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID debe ser un número.",
                "Validación", JOptionPane.WARNING_MESSAGE);
        }
    
    }//GEN-LAST:event_btsBuscarActionPerformed

    private void btsAnularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsAnularActionPerformed
        // TODO add your handling code here:
        if (facComIdActual <= 0) {
            JOptionPane.showMessageDialog(null,
                "Primero busque la factura que desea anular.",
                "Validación", JOptionPane.WARNING_MESSAGE); return;
        }
        int confirm = JOptionPane.showConfirmDialog(null,
            "¿Anular la factura " + txtNumFactura.getText() + "?\n" +
            "El registro se conserva como 'Anulada'.",
            "Confirmar Anulación",
            JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (confirm != JOptionPane.YES_OPTION) return;
 
        clsFacturaCompraAcreedor fac = new clsFacturaCompraAcreedor();
        fac.setFacComId(facComIdActual);
 
        int rows = fac.setAnularFacturaCompra(fac);
        if (rows > 0) {
            txtEstado.setText("Anulada");
            JOptionPane.showMessageDialog(null, "Factura anulada correctamente.\n",
                "Información del Sistema", JOptionPane.INFORMATION_MESSAGE);
            new BitacoraDAO().insert(idUsuario, 4027, "Anular Factura Acreedor");
            llenadoDeTablas();
            limpiarTextos();
        } else {
            JOptionPane.showMessageDialog(null, "Error al anular la factura.",
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_btsAnularActionPerformed

