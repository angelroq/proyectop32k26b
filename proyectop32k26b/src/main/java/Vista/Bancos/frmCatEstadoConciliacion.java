    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
 if (txtid.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Seleccione un estado de conciliación de la tabla para actualizar.",
            "Atención", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (!camposCompletos()) return;

    try {
        clsCatEstadoConciliacion cates = getEstadoConciliacionDeFormulario();
        cates.setCatesid(Integer.parseInt(txtid.getText().trim()));
        dao.update(cates);
        javax.swing.JOptionPane.showMessageDialog(this,
            "Estado de conciliación actualizado correctamente.",
            "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        cargarTabla();
        limpiarCampos();
    } catch (NumberFormatException ex) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "El ID debe ser un número válido.",
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Error al actualizar: " + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnActualizarActionPerformed
// ── Llena la tabla con todos los estados de conciliación ────────────────────
private void cargarTabla() {
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(
        new String[]{"ID", "Nombre Estado"}, 0
    ) {
        @Override
        public boolean isCellEditable(int r, int c) { return false; }
    };

    for (clsCatEstadoConciliacion cates : dao.listar()) {
        modelo.addRow(new Object[]{
            cates.getCatesid(),
            cates.getCatesnombreestado()
        });
    }
    jTable1.setModel(modelo);
}

// ── Limpia todos los campos ──────────────────────────────────
private void limpiarCampos() {
    txtid.setText("");
    jTextField1.setText("");
    jTable1.clearSelection();
}

// ── Valida que los campos no estén vacíos ───────────────────
private boolean camposCompletos() {
    if (jTextField1.getText().trim().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Complete todos los campos: Nombre Estado.",
            "Campos incompletos", javax.swing.JOptionPane.WARNING_MESSAGE);
        return false;
    }
    return true;
}
private void configurarSeleccionTabla() {
    jTable1.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
            int fila = jTable1.getSelectedRow();
            txtid.setText(jTable1.getValueAt(fila, 0).toString());      // ID
            jTextField1.setText(jTable1.getValueAt(fila, 1).toString()); // Nombre Estado
        }
    });
}

// ── Crea el objeto clsCatEstadoConciliacion desde los campos del formulario ─
private clsCatEstadoConciliacion getEstadoConciliacionDeFormulario() {
    clsCatEstadoConciliacion cates = new clsCatEstadoConciliacion(
        0,                            // ID lo genera MySQL con AUTO_INCREMENT
        jTextField1.getText().trim()  // Nombre Estado
    );
    return cates;
}

    
